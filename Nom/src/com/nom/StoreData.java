package com.nom;

public class StoreData {
	
	public String ID;
	public String Name;
	public String Address;
	public String City;
	public String PostalCode;
	public String ImageUrl;

	public StoreData(String ID, String Name, String Address, String City, String PostalCode, String ImageUrl){
		this.ID = ID;
		this.Name = Name;
		this.Address = Address;
		this.City = City;
		this.PostalCode = PostalCode;
		this.ImageUrl = ImageUrl;
	}
}